export interface WordData {
    id: string;
    word: string;
    phonemes: string[];
    meaning: string;
    imagePath: string;
    audioPath: string;
}

export interface UnitData {
    id: string;
    level: 'Prep' | 'CoreA' | 'CoreB';
    unitNumber: number;
    title: string;
    subtitle: string;
    targetSound: string;
    emoji: string;
    color: string;       // bg color class
    shadowColor: string; // shadow color
    words: WordData[];
    microReading: string[];
}

function w(id: string, word: string, phonemes: string[], meaning: string): WordData {
    return {
        id, word, phonemes, meaning,
        imagePath: `/assets/images/${id}.svg`,
        audioPath: `/assets/audio/${id}.mp3`,
    };
}

