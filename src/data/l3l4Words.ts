function w(
    id: string, word: string, phonemes: string[], meaning: string,
    onset?: string, rime?: string, wordFamily?: string,
): WordData {
    return {
        id, word, phonemes, meaning,
        imagePath: `/assets/images/${id}.svg`,
        audioPath: `/assets/audio/${id}.mp3`,
        ...(onset && { onset }),
        ...(rime && { rime }),
        ...(wordFamily && { wordFamily }),
    };
}

